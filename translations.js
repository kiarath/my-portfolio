/* =========================================================
   Language Engine
   ========================================================= */

const LangEngine = (() => {
  const STORAGE_KEY = 'kc-lang';
  let currentLang = localStorage.getItem(STORAGE_KEY) || 'en';

  function get(page, key) {
    const t = TRANSLATIONS[page]?.[currentLang] ?? TRANSLATIONS[page]?.['en'];
    return t?.[key] ?? key;
  }

  function getShared(key) {
    return TRANSLATIONS.shared[currentLang]?.[key] ?? TRANSLATIONS.shared['en']?.[key] ?? key;
  }

  function toggle() {
    const FADE_MS = 180;
    const root = document.documentElement;

    // Fade out
    root.classList.add('lang-fading');

    setTimeout(() => {
      // Swap language at the invisible midpoint
      currentLang = currentLang === 'en' ? 'es' : 'en';
      localStorage.setItem(STORAGE_KEY, currentLang);
      apply();
      updateToggleBtn();

      // Fade back in (next frame so browser paints the new content first)
      requestAnimationFrame(() => {
        requestAnimationFrame(() => {
          root.classList.remove('lang-fading');
        });
      });
    }, FADE_MS);
  }

  function updateToggleBtn() {
    const btn = document.getElementById('lang-toggle');
    if (!btn) return;
    const isEs = currentLang === 'es';
    btn.setAttribute('aria-label', isEs ? 'Switch to English' : 'Cambiar a Español');
    btn.querySelector('.lang-toggle__active').textContent  = isEs ? 'ES' : 'EN';
    btn.querySelector('.lang-toggle__other').textContent   = isEs ? 'EN' : 'ES';
    document.documentElement.lang = currentLang;
  }

  /* Cache English innerHTML for [data-i18n-tmpl] containers */
  function cacheEnglish() {
    document.querySelectorAll('[data-i18n-tmpl]').forEach(container => {
      if (!container.dataset.enCache) {
        container.dataset.enCache = container.innerHTML;
      }
    });
  }

  /* Apply translations by [data-i18n] attributes + template section swaps */
  function apply() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const [page, key] = el.dataset.i18n.split('.');
      const val = page === 'shared' ? getShared(key) : get(page, key);
      if (el.dataset.i18nHtml !== undefined) {
        el.innerHTML = val;
      } else {
        el.textContent = val;
      }
    });

    /* meta tags */
    document.querySelectorAll('[data-i18n-meta]').forEach(el => {
      const [page, key] = el.dataset.i18nMeta.split('.');
      el.content = page === 'shared' ? getShared(key) : get(page, key);
    });

    /* title */
    const titleEl = document.querySelector('[data-i18n-title]');
    if (titleEl) {
      const [page, key] = titleEl.dataset.i18nTitle.split('.');
      document.title = get(page, key);
    }

    /* href swaps: [data-i18n-href] */
    document.querySelectorAll('[data-i18n-href]').forEach(el => {
      const [page, key] = el.dataset.i18nHref.split('.');
      const url = page === 'shared' ? getShared(key) : get(page, key);
      if (url && url !== key) el.href = url;
    });

    /* Section template swaps: [data-i18n-tmpl] */
    document.querySelectorAll('[data-i18n-tmpl]').forEach(container => {
      const key = container.dataset.i18nTmpl;
      if (currentLang === 'es') {
        const tmpl = document.getElementById('es-' + key);
        if (tmpl) container.innerHTML = tmpl.innerHTML;
      } else {
        if (container.dataset.enCache) container.innerHTML = container.dataset.enCache;
      }
      container.classList.add('visible');
    });
  }

  function init() {
    cacheEnglish();
    apply();
    updateToggleBtn();
  }

  return { toggle, init, current: () => currentLang };
})();
